 Oliver Goldsmith, ‘The Village Schoolmaster’.

Goldsmith, like Gray, was an eighteenth-century poet, 
and his depiction of a genial village schoolteacher, 
who is viewed by the locals as a kind of demigod, 
is not one that has lasted, alas, into the modern age.
 But when Goldsmith was writing, 
learning and literacy and education for its own sake were looked up to, 
and the man who possessed their gifts was revered:

The village all declar’d how much he knew;
’Twas certain he could write, and cipher too:
Lands he could measure, terms and tides presage,
And e’en the story ran that he could gauge.
In arguing too, the parson own’d his skill,
For e’en though vanquish’d he could argue still;
While words of learned length and thund’ring sound
Amazed the gazing rustics rang’d around;
And still they gaz’d and still the wonder grew,
That one small head could carry all he knew …


